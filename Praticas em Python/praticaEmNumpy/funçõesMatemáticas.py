# funções matemáticas
import numpy as np

k = np.array([[17,22,43],[27,25,14],[15,24,32]])

# mostrar maior valor da matriz
print(k.max())

# mostrar menor valor da matriz
print(k.min())

# mostrar soma valor da matriz
print(k.sum())

# mostrar media dos valores da matriz
print(k.mean())

# mostrar desvio padrão dos valores da matriz
print(k.std())